String normalizeIngredientText(String raw) {
  var text = raw.toLowerCase().replaceAll('ё', 'е');
  text = text.replaceAll(RegExp(r'[^a-zа-я0-9\s]'), ' ');
  text = text.replaceAll(RegExp(r'\s+'), ' ').trim();
  if (text.isEmpty) {
    return text;
  }

  final tokens = text
      .split(' ')
      .where((token) => token.isNotEmpty)
      .map((token) => _tokenSynonyms[token] ?? token)
      .toList();
  final normalized = tokens.join(' ');

  return _phraseAliases[normalized] ?? normalized;
}

String stripIngredientDescriptors(String raw) {
  final normalized = raw
      .toLowerCase()
      .replaceAll('ё', 'е')
      .replaceAll(RegExp(r'\s+'), ' ')
      .trim();
  if (normalized.isEmpty) {
    return normalized;
  }

  final filteredTokens = normalized
      .split(' ')
      .where((token) => token.isNotEmpty)
      .where((token) => !_ingredientDescriptorTokens.contains(token))
      .where((token) => !_descriptorAmountTokenPattern.hasMatch(token))
      .toList();
  if (filteredTokens.isEmpty) {
    return normalized;
  }

  final stripped = filteredTokens.join(' ');
  return _phraseAliases[stripped] ?? stripped;
}

String toPairingKey(String canonicalName) {
  final normalized = normalizeIngredientText(canonicalName);
  return _pairingAliases[normalized] ?? normalized;
}

Set<String> pairedIngredientsFor(String canonicalName) {
  return _pairDictionary[toPairingKey(canonicalName)] ?? const <String>{};
}

Set<String> weaklyPairedIngredientsFor(String canonicalName) {
  return _weakPairDictionary[toPairingKey(canonicalName)] ?? const <String>{};
}

Set<String> forbiddenPairingsFor(String canonicalName) {
  return _forbiddenPairDictionary[toPairingKey(canonicalName)] ??
      const <String>{};
}

int countKnownPairings(String canonicalName, Iterable<String> availableNames) {
  final availableKeys = availableNames.map(toPairingKey).toSet();
  final pairs = pairedIngredientsFor(canonicalName);
  if (pairs.isEmpty || availableKeys.isEmpty) {
    return 0;
  }
  return pairs.where(availableKeys.contains).length;
}

Set<String> compatibleIngredientKeysForMatching(String canonicalName) {
  final normalized = normalizeIngredientText(canonicalName);
  return _matchingCompatibilities[normalized] ?? <String>{normalized};
}

